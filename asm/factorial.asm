# 计算10的阶乘 (10!)
# R1 - 累乘结果
# R2 - 终止值 (10)
# R3 - 当前值 (循环变量)

# 初始化
addi x1, x0, 1    # R1 = 1 (累乘结果初始化为1)
addi x2, x0, 10   # R2 = 10 (终止值)
addi x3, x0, 0    # R3 = 0 (循环变量初始化为0)

# 循环
# R3 = R3 + 1 (增加循环变量)
addi x3, x3, 1
# R1 = R1 * R3 (累乘)
mul x1, x1, x3
# 如果R3 != R2，则继续循环
bne x3, x2, -8

# 程序结束，R1中保存10! (应为3628800)
halt 